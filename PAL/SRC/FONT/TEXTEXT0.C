#include <string.h>

/* --------------------------------------------------------------------
                           local includes
   -------------------------------------------------------------------- */
#include "pal.h"
#include "palpriv.h"

/* --------------------------------------------------------------------
                            global variables
   -------------------------------------------------------------------- */
int (*TextExt)(int FntId, char *s) = TextExt0;


/* --------------------------------------------------------------------
                              Functions
   -------------------------------------------------------------------- */

/* ------------------------------------------------------------
   TextExt0:
   Computes text extent of a string in a built-in font
   ------------------------------------------------------------ */

int TextExt0(int FntId, char *s)
{
   return FNTW(FntId)*strlen(s);
}


