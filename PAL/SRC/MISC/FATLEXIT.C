#include <stdio.h>
#include <stdlib.h>

/* --------------------------------------------------------------------
                            msdos includes
   -------------------------------------------------------------------- */
#include <dos.h>

/* --------------------------------------------------------------------
                           local includes
   -------------------------------------------------------------------- */
#include "pal.h"
#include "palpriv.h"

/* --------------------------------------------------------------------
                           Functions
   -------------------------------------------------------------------- */

void FatalExit(char *Msg, int ExitCode)
{
   TextMode();
   fprintf(stderr, "Fatal error: %s\n", Msg);
   exit(ExitCode);
}

