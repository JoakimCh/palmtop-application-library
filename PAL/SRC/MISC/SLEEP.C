/* --------------------------------------------------------------------
   LightSleep:
   Enables/disables light-sleep (SLEEP_ON, SLEEP_OFF)
   -------------------------------------------------------------------- */

void SetLightSleep(int Mode)
{
   union REGS regs;
   struct SREGS segregs;

   regs.h.al = Mode;
   regs.h.ah = 0x4e;
   int86x(0x15, &regs, &regs, &segregs);
}

