/* --------------------------------------------------------------------
   TimeOut:
   Sets the time-out delay value  (1 second = 18.2)
   -------------------------------------------------------------------- */

void SetTimeOut(WORD Value)
{
    union REGS regs;
    struct SREGS segregs;

    regs.x.bx = Value;
    regs.h.ah = 0x46;
    int86x(0x15, &regs, &regs, &segregs);
}

