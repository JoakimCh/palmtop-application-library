/* --------------------------------------------------------------------
  SCQuit:             Terminate SC

   -------------------------------------------------------------------- */
void SCQuit( int type )
{
    union REGS regs;
    struct SREGS segregs;

    regs.h.ah = 0xDF;
    regs.x.di = 0x534C;
    regs.x.dx = 0x534C;

    regs.h.al = 14;
    regs.h.bl = type;

    intdosx( &regs, &regs, &segregs);
}
