/* --------------------------------------------------------------------
   Rectangle:
   Draw a rectangle from upper left corner (x1,y1) to lower right
   corner (x2,y2).
   -------------------------------------------------------------------- */

void Rectangle(int x1, int y1, int x2, int y2, int FillStyle)
{
   SetPos(x1, y1);
   CallInt(0x5f, 0x0500 | FillStyle, 0, x2, y2, NULL);
}
