#include <dos.h>

/* --------------------------------------------------------------------
                           local includes
   -------------------------------------------------------------------- */
#include "pal.h"
#include "palpriv.h"

/* --------------------------------------------------------------------
                           Functions
   -------------------------------------------------------------------- */

/* --------------------------------------------------------------------
   Line:
   Draw a line between two endpoints (x1,y1) and (x2,y2)
   -------------------------------------------------------------------- */

void Line(int x1, int y1, int x2, int y2)
{
   SetPos(x1, y1);
   CallInt(0x5f, 0x0600, 0, x2, y2, NULL);
}


