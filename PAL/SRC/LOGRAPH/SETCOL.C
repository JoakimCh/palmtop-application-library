/* --------------------------------------------------------------------
   SetColor:
   Select the color for subsequent operations - use the BLACK_COLOR or
   WHITE_COLOR constants.
   -------------------------------------------------------------------- */

void SetColor(int Color)
{
   PalState.sh_color = Color;    /* save current state */
   CallInt(0x5f, 0x0900 | Color, 0, 0, 0, NULL);
}



