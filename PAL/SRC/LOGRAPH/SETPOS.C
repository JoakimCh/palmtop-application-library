#include <stdlib.h>

/* --------------------------------------------------------------------
                            msdos includes
   -------------------------------------------------------------------- */
#include <dos.h>

/* --------------------------------------------------------------------
                           local includes
   -------------------------------------------------------------------- */
#include "pal.h"
#include "palpriv.h"

/* --------------------------------------------------------------------
                           Functions
   -------------------------------------------------------------------- */

void SetPos(int x, int y)
{
   PalState.sh_xpos = x;
   PalState.sh_ypos = y;   /* save current states */
   CallInt(0x5f, 0x0800, 0, x, y, NULL);
}


