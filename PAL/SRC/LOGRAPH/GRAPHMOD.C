#include "palpriv.h"

/* --------------------------------------------------------------------
                           Functions
   -------------------------------------------------------------------- */

/* --------------------------------------------------------------------
   GraphMode:
   Switch to 640x200 monochrome graphics mode.
   -------------------------------------------------------------------- */

int GraphMode(void)
{
   if(!Check5f()) return FALSE;
   CallInt(0x5f, 6, 0, 0, 0, NULL);
   return TRUE;
}

