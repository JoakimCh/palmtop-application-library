/* --------------------------------------------------------------------
   BlinkRate:
   Specifies the graphics cursor blinking rate (Normal blink = 9)
   -------------------------------------------------------------------- */
void BlinkRate(int Rate)
{
    union REGS regs;
    struct SREGS segregs;

    regs.x.cx = Rate;
    regs.x.ax = 0xdc02;
    int86x(0x10, &regs, &regs, &segregs);
}


