#include <stdlib.h>

/* --------------------------------------------------------------------
                            msdos includes
   -------------------------------------------------------------------- */
#include <dos.h>

/* --------------------------------------------------------------------
                           local includes
   -------------------------------------------------------------------- */
#include "pal.h"
#include "palpriv.h"

/* --------------------------------------------------------------------
                           Functions
   -------------------------------------------------------------------- */

/* --------------------------------------------------------------------
   PlotDot:
   Set a display pixel to current color
   -------------------------------------------------------------------- */

void PlotDot(int x, int y)
{
   CallInt(0x5f, 0x0700, 0, x, y, NULL);
}


